#!/usr/bin/env python3
"""
H5 Data Preparation Script

Reads one or more GHArchive .json files and extracts 100 triplets
for the H5 Modality-Agnostic Synthesis experiment.

Each triplet = one semantic "topic group" with all 3 formats:
  Format A (chat/)  → IssueCommentEvent body  (human prose)
  Format B (json/)  → PushEvent payload JSON   (structured data)
  Format C (logs/)  → PushEvent commit messages formatted as a log line

Multi-topic mode (default): extracts 20 triplets per topic across 5 topics
(docker, kubernetes, security, ci, database) = 100 total. This ensures
semantic diversity so Athena must form distinct chains per topic rather
than collapsing everything into one super-cluster.

Usage:
    # Multi-topic (default, recommended):
    python3 prepare_h5_data.py \\
        --input /path/to/2025-01-01-*.json \\
        --output ./data/h5

    # Single-topic (for debugging):
    python3 prepare_h5_data.py \\
        --input /path/to/2025-01-01-*.json \\
        --output ./data/h5 \\
        --topic docker \\
        --count 100

Topics available (pick one):
    kubernetes  → kubernetes, k8s, kubectl, helm, pod, cluster, ingress
    docker      → docker, container, dockerfile, image, registry
    ci          → github actions, workflow, pipeline, deploy, build failed
    security    → cve, vulnerability, security, dependabot, secret
    database    → postgres, mysql, redis, mongodb, migration, schema
"""

import argparse
import glob
import gzip
import json
import os
import re
import sys
from collections import defaultdict

# ─── Topic keyword maps ───────────────────────────────────────────────────────

TOPICS = {
    "kubernetes": [
        "kubernetes", "k8s", "kubectl", "helm", "pod", "cluster",
        "ingress", "deployment", "namespace", "kubelet", "kube",
    ],
    "docker": [
        "docker", "container", "dockerfile", "image", "registry",
        "docker-compose", "containerd", "podman",
    ],
    "ci": [
        "github actions", "workflow", "pipeline", "deploy", "build fail",
        "ci/cd", "artifact", "runner", "action failed",
    ],
    "security": [
        "cve", "vulnerability", "dependabot", "security", "secret",
        "token", "credential", "exploit", "patch",
    ],
    "database": [
        "postgres", "postgresql", "mysql", "redis", "mongodb",
        "migration", "schema", "database", "db error",
    ],
}

# ─── Extraction helpers ───────────────────────────────────────────────────────

def matches_topic(text: str, keywords: list[str]) -> bool:
    t = text.lower()
    return any(k in t for k in keywords)



# Bot signatures that produce low-quality Format A content
_BOT_PATTERNS = [
    # CI/deploy preview bots
    "vercel.com", "vercel.app", "<!-- add-pr-comment",
    "deploy preview for", "netlify", "deploy preview",
    # Security/dependency bots
    "![](https://res.cloudinary.com/snyk", "snyk.io",
    "renovatebot", "dependabot", "codspeed",
    # Badge/coverage bots
    "img.shields.io", "codecov.io", "coveralls.io",
    "code coverage", "codeclimate",
    # SonarQube / SonarCloud
    "sonarqube", "sonarcloud", "sonarsource", "sonar.io",
    "quality gate", "quality gate passed", "quality gate failed",
    # GitHub bots and triage bots
    "github-actions[bot]", "app/github-actions", "[bot]",
    "this bot triages", "bot triages prs", "lifecycle/stale",
    "lifecycle/rotten", "/lifecycle stale", "/close",
    "welcome to kubernetes", "it looks like this is your first pr",
    "pull request process documentation",
    "sig-contributor-experience",
    # Common bot signatures
    "co-authored-by:", "auto-generated", "this is an automated",
    "generated by", "generated automatically",
    # Encoded/binary content
    "eyJpc", "base64",
    # HTML bot comment blocks
    "<!-- ", "<!--\n", "<a href=", "<br><br>",
    # Performance/benchmark report bots
    "performance report", "benchmark report", "test report",
]

def _is_human_comment(body: str) -> bool:
    """Returns True only if the comment looks like a real human wrote it."""
    lower = body.lower()
    # Reject bot-generated content
    if any(p.lower() in lower for p in _BOT_PATTERNS):
        return False
    # Reject pure image/badge embeds
    if body.strip().startswith("!["):
        return False
    # Reject HTML-heavy content (bots love HTML)
    if body.count("<") > 3:
        return False
    # Require at least 10 real words (not just punctuation/markdown/URLs)
    words = [w for w in body.split()
             if len(w) > 2 and not w.startswith("http") and not w.startswith("@")]
    return len(words) >= 10


def extract_format_a(event: dict) -> str | None:
    """IssueCommentEvent → plain human comment body."""
    payload = event.get("payload", {})
    comment = payload.get("comment", {})
    body = comment.get("body", "").strip()
    issue = payload.get("issue", {})
    title = issue.get("title", "").strip()

    if not body or len(body) < 50:
        return None

    if not _is_human_comment(body):
        return None

    # Include issue title as context so the embedding has more signal
    if title:
        return f"Issue: {title}\n\n{body}"
    return body


def extract_format_b(event: dict) -> str | None:
    """PushEvent → the raw payload as compact JSON (structured data)."""
    payload = event.get("payload", {})
    repo = event.get("repo", {}).get("name", "")

    # Build a clean structured payload — drop noisy fields
    structured = {
        "event_type": "push",
        "repository": repo,
        "ref": payload.get("ref", ""),
        "size": payload.get("size", 0),
        "commits": [
            {
                "sha": c.get("sha", "")[:8],
                "message": c.get("message", "").strip(),
                "author": c.get("author", {}).get("name", ""),
            }
            for c in payload.get("commits", [])[:5]  # cap at 5 commits
            if c.get("message", "").strip()
        ],
    }

    if not structured["commits"]:
        return None

    return json.dumps(structured, indent=2)


def extract_format_c(event: dict) -> str | None:
    """PushEvent commit messages → formatted as CI/CD-style log lines."""
    payload = event.get("payload", {})
    repo = event.get("repo", {}).get("name", "unknown/repo")
    commits = payload.get("commits", [])

    if not commits:
        return None

    lines = [f"[INFO] Repository: {repo}"]
    lines.append(f"[INFO] Push received: {len(commits)} commit(s)")

    for i, c in enumerate(commits[:5]):
        msg = c.get("message", "").strip().split("\n")[0]  # first line only
        sha = c.get("sha", "")[:8]
        author = c.get("author", {}).get("name", "unknown")

        # Map common commit message patterns to log-style severity
        msg_lower = msg.lower()
        if any(w in msg_lower for w in ["fix", "error", "fail", "crash", "bug", "broken"]):
            level = "ERROR"
        elif any(w in msg_lower for w in ["warn", "deprecat", "todo", "hack"]):
            level = "WARN"
        elif any(w in msg_lower for w in ["feat", "add", "implement", "deploy", "release"]):
            level = "INFO"
        else:
            level = "DEBUG"

        lines.append(f"[{level}] commit {sha} by {author}: {msg}")

    return "\n".join(lines)


# ─── Main extraction loop ─────────────────────────────────────────────────────

def process_files(input_files: list[str], keywords: list[str], count: int) -> tuple[list, list, list]:
    """Single-topic extraction. Used by --topic mode only."""
    by_repo = defaultdict(dict)
    for path in input_files:
        print(f"Reading {path} ...")
        _scan_file(path, {0: keywords}, by_repo_per_topic={0: by_repo})

    complete = {
        repo: data
        for repo, data in by_repo.items()
        if "a" in data and "b" in data and "c" in data
    }
    print(f"  Repos with all 3 formats matched: {len(complete)}")
    triplets = list(complete.values())[:count]
    return [t["a"] for t in triplets], [t["b"] for t in triplets], [t["c"] for t in triplets]


def process_files_multi_topic(input_files: list[str], topic_keywords: dict[str, list[str]]) -> dict[str, dict]:
    """
    Reads every file ONCE and simultaneously collects triplets for all topics.
    Returns {topic: {repo: {"a":..., "b":..., "c":...}}} for complete triplets.
    """
    # topic → repo → {"a": ..., "b": ..., "c": ...}
    by_topic: dict[str, dict] = {t: defaultdict(dict) for t in topic_keywords}

    for path in input_files:
        print(f"Reading {path} ...")
        opener = gzip.open if path.endswith(".gz") else open
        with opener(path, "rt", encoding="utf-8", errors="replace") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    event = json.loads(line)
                except json.JSONDecodeError:
                    continue

                etype = event.get("type", "")
                repo = event.get("repo", {}).get("name", "")
                if not repo:
                    continue

                if etype == "IssueCommentEvent":
                    payload = event.get("payload", {})
                    comment_body = payload.get("comment", {}).get("body", "")
                    issue_title = payload.get("issue", {}).get("title", "")
                    content_text = (comment_body + " " + issue_title).lower()
                    for topic, keywords in topic_keywords.items():
                        if matches_topic(content_text, keywords):
                            a = extract_format_a(event)
                            if a and "a" not in by_topic[topic][repo]:
                                by_topic[topic][repo]["a"] = a

                elif etype == "PushEvent":
                    payload = event.get("payload", {})
                    commits_text = " ".join(
                        c.get("message", "") for c in payload.get("commits", [])
                    ).lower()
                    ref = payload.get("ref", "").lower()
                    content_text = commits_text + " " + ref
                    for topic, keywords in topic_keywords.items():
                        if matches_topic(content_text, keywords):
                            b = extract_format_b(event)
                            c = extract_format_c(event)
                            if b and c and "b" not in by_topic[topic][repo]:
                                by_topic[topic][repo]["b"] = b
                                by_topic[topic][repo]["c"] = c

    # Filter to complete triplets per topic
    result = {}
    for topic, by_repo in by_topic.items():
        complete = {
            repo: data
            for repo, data in by_repo.items()
            if "a" in data and "b" in data and "c" in data
        }
        result[topic] = complete
        print(f"  Topic {topic}: {len(complete)} complete triplets found")

    return result


def _scan_file(path, topic_idx_keywords, by_repo_per_topic):
    """Helper for single-topic process_files — not used in multi-topic path."""
    opener = gzip.open if path.endswith(".gz") else open
    with opener(path, "rt", encoding="utf-8", errors="replace") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
            except json.JSONDecodeError:
                continue
            etype = event.get("type", "")
            repo = event.get("repo", {}).get("name", "")
            if not repo:
                continue
            for idx, keywords in topic_idx_keywords.items():
                by_repo = by_repo_per_topic[idx]
                if etype == "IssueCommentEvent":
                    payload = event.get("payload", {})
                    content_text = (payload.get("comment", {}).get("body", "") + " " +
                                    payload.get("issue", {}).get("title", "")).lower()
                    if matches_topic(content_text, keywords):
                        a = extract_format_a(event)
                        if a and "a" not in by_repo[repo]:
                            by_repo[repo]["a"] = a
                elif etype == "PushEvent":
                    payload = event.get("payload", {})
                    content_text = (" ".join(c.get("message", "") for c in payload.get("commits", [])) +
                                    " " + payload.get("ref", "")).lower()
                    if matches_topic(content_text, keywords):
                        b = extract_format_b(event)
                        c = extract_format_c(event)
                        if b and c and "b" not in by_repo[repo]:
                            by_repo[repo]["b"] = b
                            by_repo[repo]["c"] = c


def write_triplets(format_a, format_b, format_c, output_dir: str, count: int):
    """
    Writes matched triplets to the data/h5/ directory structure.
    Triplets are matched by index — format_a[i], format_b[i], format_c[i]
    all share the same stem issue_{i:03d} and describe the same topic.
    """
    n = min(len(format_a), len(format_b), len(format_c), count)
    if n < count:
        print(f"⚠️  Only {n} complete triplets found (wanted {count}). "
              f"Try adding more input files from GHArchive.")

    dirs = {
        "chat": os.path.join(output_dir, "chat"),
        "json": os.path.join(output_dir, "json"),
        "logs": os.path.join(output_dir, "logs"),
    }
    for d in dirs.values():
        os.makedirs(d, exist_ok=True)

    for i in range(n):
        stem = f"issue_{i+1:03d}"
        with open(os.path.join(dirs["chat"], f"{stem}.txt"), "w") as f:
            f.write(format_a[i])
        with open(os.path.join(dirs["json"], f"{stem}.json"), "w") as f:
            f.write(format_b[i])
        with open(os.path.join(dirs["logs"], f"{stem}.log"), "w") as f:
            f.write(format_c[i])

    print(f"\n✅ Wrote {n} triplets to {output_dir}/")
    print(f"   chat/  → {n} .txt files  (Format A — human issue comments)")
    print(f"   json/  → {n} .json files (Format B — push event payloads)")
    print(f"   logs/  → {n} .log files  (Format C — CI-style log lines)")
    print()
    print("Next: run the ingestor")
    print(f"  go run main.go --exp h5 --mode pilot --data {output_dir}")


# ─── Entry point ─────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="Prepare H5 triplet data from GHArchive")
    parser.add_argument("--input", required=True,
                        help="GHArchive JSON file(s). Supports glob: './data/*.json'")
    parser.add_argument("--output", default="./data/h5",
                        help="Output directory (default: ./data/h5)")
    parser.add_argument("--topic", default=None,
                        choices=list(TOPICS.keys()),
                        help="Single topic filter. Omit for multi-topic mode (recommended).")
    parser.add_argument("--count", type=int, default=100,
                        help="Total triplets to extract (default: 100). In multi-topic mode, split evenly across topics.")
    args = parser.parse_args()

    # Expand glob patterns
    input_files = sorted(glob.glob(args.input))
    if not input_files:
        input_files = [args.input]
    if not input_files or not os.path.exists(input_files[0]):
        print(f"❌ No input files found matching: {args.input}")
        sys.exit(1)

    print(f"Input files ({len(input_files)}):")
    for f in input_files:
        print(f"  {f}")
    print()

    if args.topic:
        # ── Single-topic mode ──────────────────────────────────────────────────
        keywords = TOPICS[args.topic]
        print(f"Mode:     single-topic ({args.topic})")
        print(f"Keywords: {keywords}")
        print(f"Target:   {args.count} triplets")
        print(f"Output:   {args.output}")
        print()

        format_a, format_b, format_c = process_files(input_files, keywords, args.count)
        print(f"\nExtracted:")
        print(f"  Format A (IssueCommentEvent): {len(format_a)}")
        print(f"  Format B (PushEvent JSON):    {len(format_b)}")
        print(f"  Format C (PushEvent logs):    {len(format_c)}")

        if not format_a:
            print("\n❌ No triplets found. Try a different topic or add more input files.")
            sys.exit(1)

        write_triplets(format_a, format_b, format_c, args.output, args.count)

    else:
        # ── Multi-topic mode (default) ─────────────────────────────────────────
        # Read all files ONCE and collect for all topics simultaneously.
        topic_names = list(TOPICS.keys())
        per_topic = args.count // len(topic_names)
        remainder = args.count % len(topic_names)

        print(f"Mode:     multi-topic ({', '.join(topic_names)})")
        print(f"Target:   {args.count} triplets ({per_topic} per topic, +{remainder} remainder to first topic)")
        print(f"Output:   {args.output}")
        print(f"Strategy: single-pass read — all files read once for all topics")
        print()

        # Single pass over all files
        topic_results = process_files_multi_topic(input_files, TOPICS)
        print()

        all_a, all_b, all_c = [], [], []
        for i, topic in enumerate(topic_names):
            n = per_topic + (1 if i < remainder else 0)
            complete = topic_results.get(topic, {})
            triplets = list(complete.values())[:n]
            fa = [t["a"] for t in triplets]
            fb = [t["b"] for t in triplets]
            fc = [t["c"] for t in triplets]
            print(f"  {topic}: using {len(fa)}/{n} triplets")
            all_a.extend(fa)
            all_b.extend(fb)
            all_c.extend(fc)

        print(f"\nTotal extracted: {len(all_a)} triplets across {len(topic_names)} topics")

        if not all_a:
            print("\n❌ No triplets found across any topic. Add more input files.")
            sys.exit(1)

        write_triplets(all_a, all_b, all_c, args.output, args.count)


if __name__ == "__main__":
    main()
